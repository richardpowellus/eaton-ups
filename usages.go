// UsageName returns a human-readable name for a usage page + usage ID
func UsageName(page, usage uint32) string {
	var m map[uint32]string
	switch page {
	case 0x84:
		m = powerDeviceUsages
	case 0x85:
		m = batterySystemUsages
	case 0xFFFF:
		m = mgeVendorUsages
	default:
		return ""
	}
	if name, ok := m[usage]; ok {
		return name
	}
	return ""
}
